def _compose_operator_context() -> str:
    sections: list[str] = [
        "# Operator Context\n\n"
        "Generated by `episteme sync`. "
        "Edit sources in `~/episteme/core/memory/global/`.\n\n",
        GOVERNANCE_HEADER,
        SIGNED_SURFACE_GOVERNANCE_ADDENDUM,
    ]
    for name in _MEMORY_FILES:
        mem_file = _cli.REPO_ROOT / "core" / "memory" / "global" / name
        if mem_file.exists():
            sections.append(mem_file.read_text(encoding="utf-8").rstrip() + "\n\n")
    return "".join(sections).rstrip()


