def sync_signed_surface_bridge(hermes_root: Path) -> bool:
    """Write the signed-surface bridge artifacts under ~/.hermes/.

    Idempotent. Returns True on success. Does NOT install or run any
    cryptographic primitives — verification stays in the operator's
    primary repo where the verifier CLI lives.
    """
    if not hermes_root.exists():
        return False

    protocol_path = hermes_root / "SIGNED_SURFACE_PROTOCOL.md"
    _cli._write_managed_file(
        protocol_path,
        _SIGNED_SURFACE_PROTOCOL_MD,
        prior_signature="# Signed Reasoning Surface Protocol",
    )

    schemas_dir = hermes_root / "schemas"
    schemas_dir.mkdir(exist_ok=True)
    schema_path = schemas_dir / "signed-surface-v1.0.md"
    _cli._write_managed_file(
        schema_path,
        _SIGNED_SURFACE_SCHEMA_REF_MD,
        prior_signature="# signed-surface@1.0 — schema reference",
    )

    return True


def sync() -> bool:
    """Sync operator context + skills + signed-surface bridge into ~/.hermes if installed.

    Returns True if Hermes was found and synced, False if not installed.
    """
    hermes_root = _cli.HOME / ".hermes"
    if not hermes_root.exists():
        return False

    skills_dst = hermes_root / "skills"
    for skill_dir in _cli._managed_skills():
        _cli._copy_tree(skill_dir, skills_dst / skill_dir.name)

    operator_md = hermes_root / "OPERATOR.md"
    _cli._write_managed_file(
        operator_md,
        _compose_operator_context(),
        prior_signature="# Operator Context",
    )

    soul_path = hermes_root / "SOUL.md"
    if not soul_path.exists():
        soul_content = (
            "# Hermes Soul\n\n"
            "You are a technical AI assistant working with the operator described below.\n"
            "Load this context at the start of every session.\n\n"
            f"{{{{read {operator_md}}}}}\n"
        )
        _cli._write_text(soul_path, soul_content)
        print(f"  - Created Hermes SOUL.md: {soul_path}")

    # Signed-surface bridge — additive; failure does not break legacy sync
    try:
        sync_signed_surface_bridge(hermes_root)
    except Exception as e:
        print(f"  - WARNING: signed-surface bridge failed: {e}")

    return True


