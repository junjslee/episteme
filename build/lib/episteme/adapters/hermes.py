_SIGNED_SURFACE_PROTOCOL_MD = """# Signed Reasoning Surface Protocol — Hermes

This file is written by `episteme sync` (Hermes adapter). It documents
how a Hermes session interacts with episteme's signed-surface@1.0 workflow.
The schema reference is at `~/.hermes/schemas/signed-surface-v1.0.md`.

## What the operator does (NOT you)

Before any irreversible-class action (push, merge, deploy, migrate,
publish, external-post, db-destructive-SQL), the operator authors a
Reasoning Surface via `episteme surface author` and signs it with their
operator key. The signed artifact is persisted to
`.episteme/surfaces/<date>/<surface_id>.json` and an active-pointer is
set at `.episteme/surfaces/active.txt`.

## What you do (Hermes session)

When the operator asks you to take an irreversible action:

1. **Read the active signed surface.** Path: `.episteme/surfaces/active.txt`
   contains the active surface id; resolve to the JSON file.
2. **Check that the surface's `core_question` and `decision.choice`
   reflect the operator's current ask.** If not, stop and ask the
   operator to re-author.
3. **Cite the surface in your output** when proceeding — say, in plain
   text, that you are proceeding under the named surface id.
4. **Never author or auto-fill a surface for the operator.** The signing
   key access is structurally absent in a Hermes session; an
   auto-authored surface either fails cryptographically or gets flagged
   by the LLM-paraphrase classifier and rejected at the verifier.

## Why this design

The MIRROR benchmark (arXiv 2604.19809) demonstrated that providing an
LLM with its own calibration scores produces no significant improvement
on Confident Failure Rate. Only **external architectural constraint** is
effective. The signed Reasoning Surface IS that architectural constraint
— but only if the human operator is the one who authors it. If you
(running in Hermes) author it on the operator's behalf, the constraint
dissolves into a model self-evaluation loop, which the MIRROR finding
specifically shows does not work.

## What you can do without the operator

For *reversible* actions — file edits, debug experiments, code reading,
reasoning out loud — there is no signed-surface requirement. The
operator may still want a `reasoning-surface@1` (the legacy unsigned
form) for high-blast-radius reversible work; check
`.episteme/reasoning-surface.json` and respect it if present.

## Verifying a surface yourself

You cannot verify cryptographically without invoking
`python -m episteme.verify` from the operator's primary repo. You can
read the surface JSON and reason about its content. If you suspect a
chain break or signature anomaly, point the operator at
`episteme evidence alerts` and `episteme verify --chain ...`.
"""


