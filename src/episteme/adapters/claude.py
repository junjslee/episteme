def build_settings(governance_mode: str = "balanced") -> dict:
    hooks_dir = _cli.REPO_ROOT / "core" / "hooks"
    py = sys.executable

    def hook_cmd(script: str, *, async_: bool = False) -> dict:
        h: dict = {"type": "command", "command": f"{py} {hooks_dir / script}"}
        if async_:
            h["async"] = True
        return h

    checkpoint_cmd = f"{py} {hooks_dir / 'checkpoint.py'}"

    pretool_entries = [
        {"matcher": "Bash", "hooks": [hook_cmd("block_dangerous.py")]},
        {"matcher": "Bash|Write|Edit|MultiEdit", "hooks": [hook_cmd("reasoning_surface_guard.py")]},
        # Cognitive Arm A auto-instrumentation pre-snapshot (Event 91).
        # Snapshots watched-file content (operator_profile.md +
        # cognitive_profile.md + workflow_policy.md + agent_feedback.md)
        # so the post hook can diff and emit trajectory entries. No-op
        # for unwatched files. Pairs with `_arm_a_post.py` via
        # correlation_id (Event 50 candidate-list pattern).
        {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("_arm_a_pre.py")]},
    ]
    posttool_entries = [
        {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("format.py", async_=True)]},
        {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("test_runner.py")]},
        # Cognitive Arm A auto-instrumentation post-record (Event 91).
        # Reads marker from `_arm_a_pre.py`, diffs pre vs post, emits
        # one trajectory entry per (axis,field) change for profile or
        # per H2 section for policy. auto_recorded=True flag in payload
        # lets future audits filter manual vs automated entries.
        {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("_arm_a_post.py")]},
        # PostToolUse Bash writers (Path-A Event 38 — 2026-04-23).
        # These existed in hooks/hooks.json since CP7/CP8 but were never
        # added to build_settings(), so `episteme sync` never registered
        # them with Claude Code. 3 days of Day-2-Gate-Grading evidence
        # pointed at an async-hook bug; the actual cause was this manifest
        # gap. All 4 ship with async=False per operator's conservative
        # workaround — they complete in ≤ 50ms direct-invocation so the
        # sync latency on the hot path is negligible; flipping back to
        # async after soak-close verification is a trivial revert.
        {"matcher": "Bash", "hooks": [hook_cmd("state_tracker.py")]},
        {"matcher": "Bash", "hooks": [hook_cmd("calibration_telemetry.py")]},
        {"matcher": "Bash", "hooks": [hook_cmd("episodic_writer.py")]},
        {"matcher": "Bash", "hooks": [hook_cmd("fence_synthesis.py")]},
    ]

    mode = (governance_mode or "balanced").strip().lower()
    if mode in {"balanced", "strict"}:
        pretool_entries.extend([
            {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("workflow_guard.py")]},
            {"matcher": "Write|Edit|MultiEdit", "hooks": [hook_cmd("prompt_guard.py")]},
        ])
        posttool_entries.append(
            {"matcher": "Bash|Edit|Write|MultiEdit|Agent|Task", "hooks": [hook_cmd("context_guard.py")]}
        )

    settings = {
        "permissions": {
            "deny": [
                "Read(./.env)",
                "Read(./.env.*)",
                "Read(./secrets/**)",
                "Read(./**/*.pem)",
                "Read(./**/*.key)",
                "Read(./**/.npmrc)",
            ]
        },
        "hooks": {
            "SessionStart": [{"hooks": [hook_cmd("session_context.py")]}],
            "PreToolUse": pretool_entries,
            "PostToolUse": posttool_entries,
            "PermissionRequest": [
                {
                    "matcher": "Read|Glob|Grep",
                    "hooks": [{"type": "command", "command": "echo '{\"decision\":\"allow\"}'"}],
                }
            ],
            "PreCompact": [{"hooks": [hook_cmd("precompact_backup.py", async_=True)]}],
            "Stop": [
                {"hooks": [hook_cmd("quality_gate.py")]},
                {"hooks": [{"type": "command", "command": checkpoint_cmd}]},
            ],
            "SubagentStop": [{"hooks": [{"type": "command", "command": checkpoint_cmd}]}],
        },
    }

    if mode == "strict":
        settings["hooks"].pop("PermissionRequest", None)

    return settings


# ---------------------------------------------------------------------------
# Hook command normalization + deduplication
# ---------------------------------------------------------------------------

def normalize_hook_command(cmd: str) -> str:
    if not cmd:
        return ""
    c = cmd.replace("\\", "/")
    c = re.sub(r"^(/\S+/)(python\S*)\s+", r"python3 ", c)
    return c


def dedupe_hooks_map(hooks: dict) -> dict:
    if not isinstance(hooks, dict):
        return {}
    out: dict = {}
    for event, entries in hooks.items():
        if not isinstance(entries, list):
            continue
        deduped_entries = []
        seen = set()
        for entry in entries:
            if not isinstance(entry, dict):
                continue
            matcher = entry.get("matcher", "*")
            hook_list = entry.get("hooks", [])
            if not isinstance(hook_list, list):
                continue
            key_parts = []
            for h in hook_list:
                if not isinstance(h, dict):
                    continue
                cmd = normalize_hook_command(str(h.get("command", "")))
                h_type = h.get("type", "command")
                if cmd:
                    key_parts.append(("cmd", cmd, bool(h.get("async", False)), h_type))
                else:
                    key_parts.append(("obj", json.dumps(h, sort_keys=True, separators=(",", ":"))))
            if not key_parts:
                continue
            key = (event, matcher, tuple(key_parts))
            if key in seen:
                continue
            seen.add(key)
            deduped_entries.append(entry)
        if deduped_entries:
            out[event] = deduped_entries
    return out


# ---------------------------------------------------------------------------
# Merge user settings with episteme-managed settings
# ---------------------------------------------------------------------------

def merge_settings(existing: dict, episteme: dict) -> dict:
    """Merge episteme settings into existing without removing anything.

    - permissions.deny: union (no duplicates)
    - hooks.<event>: append episteme entries whose commands aren't already present
    - hook dedupe: remove exact duplicate entries
    - All other existing keys: preserved untouched
    """
    import copy
    merged = copy.deepcopy(existing)

    deny = merged.setdefault("permissions", {}).setdefault("deny", [])
    for rule in episteme.get("permissions", {}).get("deny", []):
        if rule not in deny:
            deny.append(rule)

    for event, entries in episteme.get("hooks", {}).items():
        existing_entries = merged.setdefault("hooks", {}).setdefault(event, [])
        registered_cmds: set[str] = set()
        for entry in existing_entries:
            for h in entry.get("hooks", []):
                registered_cmds.add(normalize_hook_command(h.get("command", "")))
        for entry in entries:
            new_cmds = {normalize_hook_command(h.get("command", "")) for h in entry.get("hooks", [])}
            if not new_cmds.issubset(registered_cmds):
                existing_entries.append(entry)

    merged["hooks"] = dedupe_hooks_map(merged.get("hooks", {}))
    return merged


# ---------------------------------------------------------------------------
# Governance pack enforcement
# ---------------------------------------------------------------------------

def _collect_managed_hook_commands(settings: dict) -> set[str]:
    commands: set[str] = set()
    hooks = settings.get("hooks", {}) if isinstance(settings, dict) else {}
    if not isinstance(hooks, dict):
        return commands
    for entries in hooks.values():
        if not isinstance(entries, list):
            continue
        for entry in entries:
            if not isinstance(entry, dict):
                continue
            hook_list = entry.get("hooks", [])
            if not isinstance(hook_list, list):
                continue
            for h in hook_list:
                if not isinstance(h, dict):
                    continue
                cmd = normalize_hook_command(str(h.get("command", "")))
                if cmd:
                    commands.add(cmd)
    return commands


def prune_managed_hook_entries(settings: dict, governance_mode: str) -> dict:
    """Keep external hooks, but remove managed hooks not in the selected pack."""
    mode = (governance_mode or "balanced").strip().lower()
    expected = _collect_managed_hook_commands(build_settings(mode))
    managed_any: set[str] = set()
    for m in ("minimal", "balanced", "strict"):
        managed_any.update(_collect_managed_hook_commands(build_settings(m)))

    hooks = settings.get("hooks")
    if not isinstance(hooks, dict):
        return settings

    pruned: dict = {}
    for event, entries in hooks.items():
        if not isinstance(entries, list):
            pruned[event] = entries
            continue

        out_entries = []
        for entry in entries:
            if not isinstance(entry, dict):
                out_entries.append(entry)
                continue

            hook_list = entry.get("hooks", [])
            if not isinstance(hook_list, list):
                out_entries.append(entry)
                continue

            kept_hooks = []
            for h in hook_list:
                if not isinstance(h, dict):
                    kept_hooks.append(h)
                    continue
                cmd = normalize_hook_command(str(h.get("command", "")))
                if cmd and cmd in managed_any and cmd not in expected:
                    continue
                kept_hooks.append(h)

            if kept_hooks:
                new_entry = dict(entry)
                new_entry["hooks"] = kept_hooks
                out_entries.append(new_entry)

        if out_entries:
            pruned[event] = out_entries

    settings["hooks"] = pruned
    return settings


def _is_default_permission_allow_hook(hook: dict) -> bool:
    if not isinstance(hook, dict):
        return False
    if str(hook.get("type", "command")) != "command":
        return False
    cmd = normalize_hook_command(str(hook.get("command", ""))).strip().lower()
    return cmd.startswith("echo ") and "decision" in cmd and "allow" in cmd


def enforce_governance_overrides(settings: dict, governance_mode: str) -> dict:
    """Apply governance-mode overrides that must win over additive merges."""
    mode = (governance_mode or "balanced").strip().lower()
    if mode != "strict":
        return settings

    hooks = settings.get("hooks")
    if isinstance(hooks, dict):
        current = hooks.get("PermissionRequest")
        if isinstance(current, list):
            kept_entries = []
            for entry in current:
                if not isinstance(entry, dict):
                    kept_entries.append(entry)
                    continue
                matcher = str(entry.get("matcher", "")).strip()
                hook_list = entry.get("hooks", [])
                if matcher != "Read|Glob|Grep" or not isinstance(hook_list, list):
                    kept_entries.append(entry)
                    continue

                filtered = [h for h in hook_list if not _is_default_permission_allow_hook(h)]
                if filtered:
                    new_entry = dict(entry)
                    new_entry["hooks"] = filtered
                    kept_entries.append(new_entry)

            if kept_entries:
                hooks["PermissionRequest"] = kept_entries
            else:
                hooks.pop("PermissionRequest", None)
    return settings


# ---------------------------------------------------------------------------
# Sync entry point
# ---------------------------------------------------------------------------

def sync(governance_mode: str = "balanced") -> None:
    claude_root = _cli.HOME / ".claude"

    _cli._write_managed_file(
        claude_root / "CLAUDE.md",
        render_user_claude_md(),
        prior_signature="# episteme Global Memory",
    )

    settings_path = claude_root / "settings.json"
    managed = build_settings(governance_mode)
    if settings_path.exists():
        try:
            existing = json.loads(settings_path.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            existing = {}
    else:
        existing = {}
    merged = merge_settings(existing, managed)
    merged = prune_managed_hook_entries(merged, governance_mode)
    merged = enforce_governance_overrides(merged, governance_mode)
    merged["hooks"] = dedupe_hooks_map(merged.get("hooks", {}))
    _cli._write_text(settings_path, json.dumps(merged, indent=2) + "\n")

    for agent_file in (_cli.REPO_ROOT / "core" / "agents").glob("*.md"):
        _cli._copy_file(agent_file, claude_root / "agents" / agent_file.name)

    for skill_dir in _cli._managed_skills():
        _cli._copy_tree(skill_dir, claude_root / "skills" / skill_dir.name)


