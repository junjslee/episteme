"""Claude Code adapter. Mounts the operator context into ~/.claude and
manages the Claude-format settings.json (permissions, hooks, agents,
skills).
"""
from __future__ import annotations

import json
import re
import sys

from .. import cli as _cli


# ---------------------------------------------------------------------------
# Render: operator memory index
# ---------------------------------------------------------------------------

def render_user_claude_md() -> str:
    names = ["overview", "operator_profile", "workflow_policy", "python_runtime_policy", "cognitive_profile", "agent_feedback"]
    imports = "\n".join(f"@{_cli._resolve_memory_file(name)}" for name in names)
    status, age, elicited = _cli._profile_staleness()
    warning = _cli._render_stale_profile_warning(status, age, elicited)
    warning_block = (warning + "\n") if warning else ""
    return (
        "# episteme Global Memory\n\n"
        "This file is generated by `episteme sync`.\n"
        "Edit the source of truth in `~/episteme/core/memory/global/`.\n"
        f"{warning_block}\n"
        f"{imports}\n"
    )


